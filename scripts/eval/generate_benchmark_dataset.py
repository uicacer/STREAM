def _save_output(existing_queries: list, new_queries: list, max_id: int, target: int) -> list:
    """Merge existing + new queries, assign IDs, write OUTPUT_FILE, return merged list."""
    original_formatted = []
    for q in existing_queries:
        oq = dict(q)
        oq.setdefault("generated_by", "claude-sonnet (original)")
        oq.setdefault("generated_at", "2026-05-15")
        original_formatted.append(oq)

    all_new_formatted = []
    next_id = max_id + 1
    for q in new_queries:
        all_new_formatted.append(
            {
                "id": next_id,
                "text": q["text"],
                "ground_truth": q["ground_truth"],
                "domain": q["domain"],
                "generated_by": q.get("generated_by", LABELING_MODEL),
                "generated_at": q.get("generated_at", utcnow()),
            }
        )
        next_id += 1

    merged = original_formatted + all_new_formatted

    output = {
        "_description": (
            "STREAM routing benchmark dataset. "
            f"{len(merged)} queries across {len(DOMAINS)} domains × 3 complexity levels. "
            f"Target: {target} per cell for ±{100*(1.96*0.5/target**0.5):.0f}pp CIs."
        ),
        "_labeling_criteria": LABELING_CRITERIA,
        "_domains": DOMAINS,
        "_labeling_model": LABELING_MODEL,
        "_labeling_method": (
            f"Queries generated and labeled by {LABELING_MODEL} using a structured rubric. "
            "Original 1,800 queries were spot-checked by authors. "
            "A 5% cross-model consistency check (see consistency_report.json) "
            "using GPT-4o-mini is provided as a label-reliability proxy."
        ),
        "_stats": {
            "total": len(merged),
            "original": len(original_formatted),
            "generated": len(all_new_formatted),
            "domains": len(DOMAINS),
            "classes": 3,
            "target_per_cell": target,
            "generated_at": utcnow(),
        },
        "queries": merged,
    }

    with open(OUTPUT_FILE, "w") as f:
        json.dump(output, f, indent=2)

    return merged


