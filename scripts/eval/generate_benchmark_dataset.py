def build_labeling_prompt(queries: list[str]) -> str:
    criteria_block = "\n".join(f"  - {k}: {v}" for k, v in LABELING_CRITERIA.items())
    queries_block = "\n".join(f"{i+1}. {q}" for i, q in enumerate(queries))

    return f"""You are classifying user queries by complexity for an AI routing system.

Complexity levels:
{criteria_block}

Classify each query below as LOW, MEDIUM, or HIGH.
Return ONLY a JSON array of strings, one label per query in the same order.
Example: ["LOW", "HIGH", "MEDIUM"]

Queries:
{queries_block}"""


