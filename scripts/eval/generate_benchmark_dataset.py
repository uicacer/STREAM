def generate_queries_for_cell(
    domain: str,
    complexity: str,
    existing_texts: list[str],
    n_needed: int,
    dry_run: bool = False,
    batch_size: int = 30,
) -> list[dict]:
    """Generate n_needed new queries for a (domain, complexity) cell."""
    generated = []
    existing_set = {t.lower().strip() for t in existing_texts}
    consecutive_empty = 0
    max_consecutive_empty = 10  # stop only after 10 full batches with zero new queries

    print(f"  Generating {n_needed} queries for {domain}/{complexity}...")

    while len(generated) < n_needed:
        this_batch = min(batch_size, n_needed - len(generated))
        if dry_run:
            for _i in range(this_batch):
                generated.append(
                    {
                        "text": f"[DRY RUN] {domain} {complexity} query #{len(generated)+1}",
                        "domain": domain,
                        "ground_truth": complexity,
                        "generated_by": LABELING_MODEL,
                        "generated_at": utcnow(),
                    }
                )
            break

        prompt = build_generation_prompt(domain, complexity, existing_texts, this_batch)

        try:
            response = call_anthropic(prompt)
            batch = safe_json_parse(response)

            if not isinstance(batch, list):
                print("    [WARN] Non-list response, retrying...")
                time.sleep(2)
                continue

            new_this_batch = 0
            for text in batch:
                if not isinstance(text, str) or len(text) < 10:
                    continue
                if text.lower().strip() in existing_set:
                    continue  # skip exact duplicate
                existing_set.add(text.lower().strip())
                generated.append(
                    {
                        "text": text.strip(),
                        "domain": domain,
                        "ground_truth": complexity,
                        "generated_by": LABELING_MODEL,
                        "generated_at": utcnow(),
                    }
                )
                new_this_batch += 1
                if len(generated) >= n_needed:
                    break

            print(f"    Progress: {len(generated)}/{n_needed}")
            time.sleep(0.5)  # rate limit buffer

            if new_this_batch == 0:
                consecutive_empty += 1
                if consecutive_empty >= max_consecutive_empty and len(generated) >= n_needed * 0.99:
                    print(
                        f"    [INFO] No new unique queries after {max_consecutive_empty} consecutive batches — stopping at {len(generated)}/{n_needed}"
                    )
                    break
            else:
                consecutive_empty = 0  # reset on any progress

        except Exception as e:
            print(f"    [ERROR] {e}, retrying in 5s...")
            time.sleep(5)

    return generated[:n_needed]


# ---------------------------------------------------------------------------
# Consistency check
# ---------------------------------------------------------------------------


def cohen_kappa(labels_a: list[str], labels_b: list[str]) -> float:
    """Compute Cohen's kappa for two sequences of categorical labels."""
    assert len(labels_a) == len(labels_b)
    n = len(labels_a)
    classes = sorted(set(labels_a) | set(labels_b))

    # Observed agreement
    po = sum(a == b for a, b in zip(labels_a, labels_b, strict=False)) / n

    # Expected agreement
    count_a = Counter(labels_a)
    count_b = Counter(labels_b)
    pe = sum((count_a[c] / n) * (count_b[c] / n) for c in classes)

    if pe == 1.0:
        return 1.0
    return (po - pe) / (1 - pe)


def run_consistency_check(
    all_queries: list[dict], sample_frac: float = 0.05, dry_run: bool = False
) -> dict:
    """Re-label a random 5% sample with GPT-4o-mini and compute Cohen's kappa."""
    sample_n = max(50, int(len(all_queries) * sample_frac))
    sample = random.sample(all_queries, sample_n)
    texts = [q["text"] for q in sample]
    original_labels = [q["ground_truth"] for q in sample]

    print(f"\nConsistency check: re-labeling {sample_n} queries with {CONSISTENCY_MODEL}...")

    if dry_run:
        # Simulate near-perfect agreement
        check_labels = [
            lbl if random.random() > 0.1 else random.choice(CLASSES) for lbl in original_labels
        ]
    else:
        # Process in batches of 20 — smaller batches reduce length-mismatch failures
        # and make it easier for GPT-4o-mini to stay on-format.
        check_labels = []
        batch_size = 20
        n_batches = (len(texts) + batch_size - 1) // batch_size
        for i in range(0, len(texts), batch_size):
            batch_texts = texts[i : i + batch_size]
            prompt = build_labeling_prompt(batch_texts)
            batch_idx = i // batch_size
            try:
                response = call_openai(prompt)
                batch_labels = safe_json_parse(response)

                if not isinstance(batch_labels, list):
                    print(
                        f"  [WARN] Batch {batch_idx}/{n_batches}: could not parse response, skipping"
                    )
                    check_labels.extend(["UNKNOWN"] * len(batch_texts))
                    continue

                # Normalize labels: uppercase, strip whitespace, map common variants
                _norm = {"low": "LOW", "medium": "MEDIUM", "med": "MEDIUM", "high": "HIGH"}
                batch_labels = [
                    _norm.get(str(lbl).strip().lower(), str(lbl).strip().upper())
                    for lbl in batch_labels
                ]

                if len(batch_labels) != len(batch_texts):
                    print(
                        f"  [WARN] Batch {batch_idx}/{n_batches}: expected {len(batch_texts)} labels, got {len(batch_labels)}"
                    )
                    # Pad or trim to match batch length
                    if len(batch_labels) < len(batch_texts):
                        batch_labels.extend(["UNKNOWN"] * (len(batch_texts) - len(batch_labels)))
                    else:
                        batch_labels = batch_labels[: len(batch_texts)]

                check_labels.extend(batch_labels)
                print(
                    f"  Batch {batch_idx+1}/{n_batches}: {len([lbl for lbl in batch_labels if lbl in CLASSES])}/{len(batch_texts)} valid labels"
                )

            except Exception as e:
                print(f"  [ERROR] Batch {batch_idx}/{n_batches} failed: {e}")
                check_labels.extend(["UNKNOWN"] * len(batch_texts))
            time.sleep(0.3)

    # Filter out UNKNOWN labels
    valid = [(o, c) for o, c in zip(original_labels, check_labels, strict=False) if c in CLASSES]
    if not valid:
        return {"error": "No valid labels returned"}

    orig_valid, check_valid = zip(*valid, strict=False)
    kappa = cohen_kappa(list(orig_valid), list(check_valid))
    agreement = sum(a == b for a, b in valid) / len(valid)

    per_class = {}
    for cls in CLASSES:
        cls_pairs = [(o, c) for o, c in valid if o == cls]
        if cls_pairs:
            per_class[cls] = {
                "n": len(cls_pairs),
                "agreement": sum(o == c for o, c in cls_pairs) / len(cls_pairs),
            }

    interpretation = (
        "near-perfect"
        if kappa >= 0.80
        else "substantial"
        if kappa >= 0.60
        else "moderate"
        if kappa >= 0.40
        else "fair/poor — consider reviewing labeling rubric"
    )

    report = {
        "primary_model": LABELING_MODEL,
        "consistency_model": CONSISTENCY_MODEL,
        "n_checked": len(valid),
        "n_invalid": len(check_labels) - len(valid),
        "agreement": round(agreement, 4),
        "cohens_kappa": round(kappa, 4),
        "interpretation": interpretation,
        "per_class": per_class,
        "timestamp": utcnow(),
    }

    print(f"  Agreement: {agreement:.1%}, Cohen's κ = {kappa:.3f} ({interpretation})")
    return report


