def call_anthropic(prompt: str, model: str = LABELING_MODEL, max_tokens: int = 4096) -> str:
    """Call Anthropic API and return the text content."""
    import anthropic

    client = anthropic.Anthropic()
    msg = client.messages.create(
        model=model,
        max_tokens=max_tokens,
        messages=[{"role": "user", "content": prompt}],
    )
    return msg.content[0].text.strip()


def call_openai(prompt: str, model: str = CONSISTENCY_MODEL, max_tokens: int = 2048) -> str:
    """Call OpenAI API and return the text content."""
    from openai import OpenAI

    client = OpenAI()
    resp = client.chat.completions.create(
        model=model,
        max_tokens=max_tokens,
        messages=[{"role": "user", "content": prompt}],
    )
    return resp.choices[0].message.content.strip()


def safe_json_parse(text: str) -> list | None:
    """Parse a JSON array from an API response, handling common LLM formatting quirks."""
    import re

    text = text.strip()

    # Strip ```json ... ``` or ``` ... ``` fences
    if text.startswith("```"):
        lines = text.split("\n")
        text = "\n".join(lines[1:-1] if lines[-1].strip() == "```" else lines[1:])
        text = text.strip()

    # Try direct parse first
    try:
        result = json.loads(text)
        if isinstance(result, list):
            return result
        # GPT-4o-mini sometimes returns {"labels": [...]} or {"classifications": [...]}
        if isinstance(result, dict):
            for v in result.values():
                if isinstance(v, list):
                    return v
    except json.JSONDecodeError:
        pass

    # Extract the first JSON array from anywhere in the text using regex.
    # Handles cases where GPT adds explanation text before or after the array.
    match = re.search(r"\[.*?\]", text, re.DOTALL)
    if match:
        try:
            result = json.loads(match.group())
            if isinstance(result, list):
                return result
        except json.JSONDecodeError:
            pass

    return None


