def main():
    parser = argparse.ArgumentParser(description="Generate STREAM benchmark dataset")
    parser.add_argument(
        "--dry-run", action="store_true", help="Generate fake queries (no API calls)"
    )
    parser.add_argument(
        "--cells", type=int, default=None, help="Limit to first N cells (for testing)"
    )
    parser.add_argument(
        "--check-only", action="store_true", help="Only run consistency check on existing output"
    )
    parser.add_argument(
        "--target",
        type=int,
        default=TARGET_PER_CELL,
        help=f"Target per cell (default {TARGET_PER_CELL})",
    )
    args = parser.parse_args()

    if not args.dry_run:
        if not os.environ.get("ANTHROPIC_API_KEY"):
            raise OSError("ANTHROPIC_API_KEY not set")
        if not os.environ.get("OPENAI_API_KEY"):
            print("[WARN] OPENAI_API_KEY not set — consistency check will be skipped")

    # Load base dataset — resume from partial output if it exists, otherwise start fresh
    if OUTPUT_FILE.exists() and not args.dry_run:
        with open(OUTPUT_FILE) as f:
            existing_data = json.load(f)
        existing_queries = existing_data["queries"]
        # Strip any dry-run placeholder queries left from previous test runs
        before = len(existing_queries)
        existing_queries = [q for q in existing_queries if "[DRY RUN]" not in q.get("text", "")]
        if len(existing_queries) < before:
            print(f"Removed {before - len(existing_queries)} dry-run placeholder queries")
        print(
            f"Resuming from {OUTPUT_FILE.name}: {len(existing_queries)} queries already generated"
        )
    else:
        with open(EXISTING_FILE) as f:
            existing_data = json.load(f)
        existing_queries = existing_data["queries"]
        print(f"Starting fresh from {EXISTING_FILE.name}: {len(existing_queries)} queries")

    # Assign new IDs starting after existing max
    max_id = max(q["id"] for q in existing_queries)

    # Build lookup: (domain, class) → list of texts
    cell_texts: dict[tuple, list[str]] = defaultdict(list)
    for q in existing_queries:
        cell_texts[(q["domain"], q["ground_truth"])].append(q["text"])

    if args.check_only:
        if not OUTPUT_FILE.exists():
            print(f"No output file found at {OUTPUT_FILE}")
            return
        with open(OUTPUT_FILE) as f:
            merged = json.load(f)
        report = run_consistency_check(merged["queries"], dry_run=args.dry_run)
        with open(CONSISTENCY_FILE, "w") as f:
            json.dump(report, f, indent=2)
        print(f"Consistency report saved to {CONSISTENCY_FILE}")
        return

    # Determine cells to process
    all_cells = [(d, c) for d in DOMAINS for c in CLASSES]
    if args.cells:
        all_cells = all_cells[: args.cells]

    new_queries = []
    total_needed = 0

    for domain, complexity in all_cells:
        existing_in_cell = cell_texts[(domain, complexity)]
        n_have = len(existing_in_cell)
        n_needed = max(0, args.target - n_have)
        total_needed += n_needed

        if n_needed == 0:
            print(f"  {domain}/{complexity}: already have {n_have} ≥ {args.target}, skipping")
            continue

        generated = generate_queries_for_cell(
            domain, complexity, existing_in_cell, n_needed, dry_run=args.dry_run
        )
        new_queries.extend(generated)

        # Save incrementally after each cell so resume works on interruption
        _save_output(existing_queries, new_queries, max_id, args.target)
        print(f"    [SAVED] {domain}/{complexity} checkpoint written")

    print(f"\nGenerated {len(new_queries)} new queries (target was {total_needed})")

    # Final save + distribution check
    merged_queries = _save_output(existing_queries, new_queries, max_id, args.target)

    dist = Counter((q["domain"], q["ground_truth"]) for q in merged_queries)
    print("\nFinal distribution:")
    for key in sorted(dist.keys()):
        flag = " ✓" if dist[key] >= args.target else f" ← only {dist[key]}"
        print(f"  {key[0]:30s} {key[1]:8s} {dist[key]}{flag}")

    # Run consistency check on merged dataset
    if os.environ.get("OPENAI_API_KEY") or args.dry_run:
        report = run_consistency_check(merged_queries, dry_run=args.dry_run)
        with open(CONSISTENCY_FILE, "w") as f:
            json.dump(report, f, indent=2)
        print(f"Consistency report saved to {CONSISTENCY_FILE}")
    else:
        print("\n[SKIPPED] Consistency check — set OPENAI_API_KEY to enable")

    print("\nDone.")
    print(f"  Dataset: {OUTPUT_FILE}")
    print(f"  Consistency: {CONSISTENCY_FILE}")
    print("\nPaper sentence:")
    print(f"  'We generated {len(new_queries)} additional queries using {LABELING_MODEL}")
    print(f"   to reach n={args.target} per cell (±5pp 95% CI). A 5\\% cross-model")
    print("   consistency check with GPT-4o-mini yielded Cohen's κ = [see report].'")


if __name__ == "__main__":
    main()
